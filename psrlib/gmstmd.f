*DECK GMSTMD
C
C
C
      DOUBLE PRECISION FUNCTION GMSTMD(IDATE)
C
C CALCULATE MEAN GST AT 0 UT ON SPECIFIED DATE.
C
      DOUBLE PRECISION T
C
C NO. OF CENTURIES FROM 1900.
C
      T=(DBLE(IDYDIF(500101,IDATE))+DBLE(18262.5))/DBLE(36525.0)
C
C CALCULATE GMST.
C
      T=DBLE(23925.836)+DBLE(8640184.542)*T+DBLE(0.0929)*T*T
      GMSTMD=MOD(T,DBLE(86400.0))
      RETURN
C
C END OF FUNCTION GMSTMD.
C
      END
